package com.tmindtech.api.demoserver.example.model;

public class Message {
    public String id;
    public String name;
    public Integer age;
    public String job;

    public Message(String id, String name, Integer age, String job) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.job = job;
    }
}
